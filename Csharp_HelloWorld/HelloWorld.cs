using System;
// Basic 'first C# program' that prints a greeting message to the console.
// The class HelloWorld is the entry point of the program.
class HelloWorld
{
    // The Main method is the starting point of execution.
    static void Main()
    {
        // The Console.WriteLine() statement is used to display output.
        // In this case, it prints the string "Hello, World!" to the console.
        Console.WriteLine("Hello, World!");
    }
}