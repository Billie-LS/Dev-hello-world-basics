// Basic 'first Java program' that prints a greeting message to the console.

// The class HelloWorld is the entry point of the program.
public class HelloWorld {
    // The main method is the starting point of execution.
    public static void main(String[] args) {
        // The System.out.println() statement is used to display output.
        // In this case, it prints the string "Hello, World!" to the console.
        System.out.println("Hello, World!");
    }
}
